
//************************************************************************************************************
//************************************************************************************************************

	#include <iostream>
	#include <cmath>
	#include <fstream>
	#include <sstream>

	using namespace std;

//************************************************************************************************************
//************************************************************************************************************

int main(){

	ofstream outfile("output.txt");

	int N = 30, n, m = 14;
	float p[N][N], p_max = 0.0, H_max = 0.0, H_obs = 0.0, I = 0.0, a = 0.5, b = 1.0, K, X;

//************************************************************************************************************
//************************************************************************************************************
	
	for (int i = 0; i < N; i++){
		for (int j = 0; j <= i; j++){
			if (j < m){
				if (j == 0) p[i][j] = 1.0;
				else p[i][j] = 0.0;
			}

			else{
				n = (i + 1) - m;
				K = pow((n / (n + a)),b);
				p[i][j] = K / float(n);
			}
		}
	}

	for (int i = 0; i < N; i++){
		for (int j = 0; j <= i; j++){
			p_max = 1.0 / float(i + 1);
			H_max = H_max + (-p_max * log2(p_max));

			X = -p[i][j] * log2(p[i][j]);
			if (p[i][j] == 0.0) X = 0.0;

			H_obs = H_obs + (X);
		}

		I = H_max - H_obs;

		outfile << i + 1 << '\t' << H_max << '\t' << H_obs << '\t' << I << endl;

		H_max = 0.0;
		H_obs = 0.0;
	}

//************************************************************************************************************

	outfile.close();

return 0;
}


